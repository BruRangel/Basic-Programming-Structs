# str.format() = optional method that gives users more control when displaying output

num = 1000

print(f"{num:.3f}")
print(f"{num:,}")   # add commas after 3 numbers
print(f"{num:b}")   # displays number in binary
print(f"{num:o}")   # displays number in octal
print(f"{num:X}")   # displays number in hexadecimal
print(f"{num:E}")   # displays number in scientific notation
